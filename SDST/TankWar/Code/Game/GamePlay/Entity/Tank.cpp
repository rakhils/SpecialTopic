void Tank::Update(float deltaTime)
{
	m_timeElapsed += deltaTime;
	if(m_timeElapsed > 2)
	{
		DebugDraw::GetInstance()->DebugRenderPoint(m_transform.GetWorldPosition(), 4, Rgba::GREEN, Rgba::RED);
		m_timeElapsed = 0;
	}
	GameObject::Update(deltaTime);
}

