void OrthographicCamera::ZoomIn(Vector2 value)
{
	m_xZoomIn += value.x;
	m_yZoomIn += value.y;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2018/06/09
*@purpose : NIL
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void OrthographicCamera::ZoomOut(Vector2 value)
{
	m_xZoomIn -= value.x;
	m_yZoomIn -= value.y;
}
