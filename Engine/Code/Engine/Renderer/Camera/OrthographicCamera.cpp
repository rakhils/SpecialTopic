////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2018/06/09
*@purpose : NIL
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void OrthographicCamera::ZoomIn(float deltaTime)
{
	m_xZoomIn += 50 * deltaTime;
	m_yZoomIn += 50 * deltaTime;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2018/06/09
*@purpose : NIL
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void OrthographicCamera::ZoomOut(float deltaTime)
{
	m_xZoomIn -= 50*deltaTime;
	m_yZoomIn -= 50*deltaTime;
}
