#include "Engine/Net/UDP/PacketTracker.hpp"
#include "Engine/Core/Time.hpp"
// CONSTRUCTOR
PacketTracker::PacketTracker(uint16_t ack)
{
	m_ackID = ack;
	m_sendTime = (GetCurrentTimeSeconds());
	m_valid = true;
}

// DESTRUCTOR
PacketTracker::~PacketTracker()
{
	m_valid = false;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2018/11/11
*@purpose : Adds realiables
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void PacketTracker::AddReliables(uint16_t reliableID)
{
	m_sentReliableIDs[m_reliablesInPacket] = reliableID;
	m_reliablesInPacket++;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2018/11/11
*@purpose : NIL
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void PacketTracker::SetReliableID(int index, uint16_t reliableID)
{
	m_sentReliableIDs[index] = reliableID;
}
