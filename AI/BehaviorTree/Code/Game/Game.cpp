#include "Game.hpp"
Game *Game::s_game = nullptr;

Game::Game()
{
	
}

Game::~Game()
{

}

// INIT GAME
void Game::Initialize()
{
	m_map = new Map();
	m_gameMode = GAME_PLAY;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*DATE    : 2019/03/16
*@purpose : Updates main menu
*@param   : NIL
*@return  : NIL
*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Game::UpdateMainMenu(float deltaTime)
{
	UNUSED(deltaTime);
}

void Game::Update(float deltaTime)
{
	if(!m_init)
	{
		Initialize();
		m_init = true;
	}
	
	switch (m_gameMode)
	{
	case MAIN_MENU:
		UpdateMainMenu(deltaTime);
		break;
	case GAME_PLAY:
		UpdateGame(deltaTime);
		break;
	default:
		break;
	}
}


void Game::Render()
{
	switch (m_gameMode)
	{
	case MAIN_MENU:
		RenderMainMenu();
		break;
	case GAME_PLAY:
		RenderGame();
		break;
	default:
		break;
	}
}

